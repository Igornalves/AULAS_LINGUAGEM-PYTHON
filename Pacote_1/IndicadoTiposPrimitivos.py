
# codigo que indica os tipos primitivos em python:

print('\n')

E = input('digite algo:')

print('seu tipo primitivo desse valor é',type(E))

print('tem espacos ?',E.isspace())

print('é um numero ?',E.isnumeric())

print('é alfabetico ?',E.isalpha())

print('estar em maiúsculas ?',E.isupper())

print('estar em minusculo ?',E.islower())

print('esta capitalizada ?',E.istitle())

print('\n')